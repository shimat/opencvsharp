namespace OpenCvSharp;

/// <summary>
/// an enum to specify how to rotate the array.
/// </summary>
public enum RotateFlags
{
    /// <summary>
    /// Rotate 90 degrees clockwise
    /// </summary>
    Rotate90Clockwise = 0,

    /// <summary>
    /// Rotate 180 degrees clockwise
    /// </summary>
    Rotate180 = 1,

    /// <summary>
    /// Rotate 270 degrees clockwise
    /// </summary>
    Rotate90Counterclockwise = 2,
};
